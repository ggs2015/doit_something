package thread.support;

public class Board {
	private Double maxX;
	private Double maxY;
	
	public boolean hasConverged(){
		return false;
	}
	public Double getMaxX() {
		return maxX;
	}
	public void setMaxX(Double maxX) {
		this.maxX = maxX;
	}
	public Double getMaxY() {
		return maxY;
	}
	public void setMaxY(Double maxY) {
		this.maxY = maxY;
	}
	
	

}
